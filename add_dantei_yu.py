def main():
    p = os.path.join(DIR, "content_bank_yu.json")
    bank = json.load(open(p, encoding="utf-8"))
    existing = {t["segments"][0].split("\n")[0].strip() for t in bank}
    n = 0
    for i, segs in enumerate(ZA):
        if segs[0].split("\n")[0].strip() in existing:
            continue
        bank.append({"type": "dantei", "cat": "education",
                     "segments": segs + [CTAS_ZA[i % len(CTAS_ZA)]]})
        n += 1
    for i, segs in enumerate(YO):
        if segs[0].split("\n")[0].strip() in existing:
            continue
        bank.append({"type": "dantei", "cat": "education",
                     "segments": segs + [CTAS_YO[i % len(CTAS_YO)]]})
        n += 1
    json.dump(bank, open(p, "w"), ensure_ascii=False, indent=1)
    print(f"断定調(坐骨5+腰痛5) {n}本追加 / バンク計 {len(bank)}")


if __name__ == "__main__":
    main()
