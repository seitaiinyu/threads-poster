def main():
    p = os.path.join(DIR, "content_bank.json")
    bank = json.load(open(p, encoding="utf-8"))
    # 既存の local を除去
    before = len(bank)
    bank = [t for t in bank if not t.get("local")]
    removed = before - len(bank)
    existing = {t["segments"][0].split("\n")[0].strip() for t in bank}

    n = 0
    for i, (cat, target, edu, summ) in enumerate(ITEMS):
        area = AREAS[i % len(AREAS)]
        hook = f"{area}にお住まいで、\n{target}あなたへ。"
        if hook.split("\n")[0].strip() in existing:
            pass
        bank.append({"type": "local", "cat": cat, "local": True,
                     "segments": [hook, edu, summ]})
        n += 1
    json.dump(bank, open(p, "w"), ensure_ascii=False, indent=1)
    print(f"旧local {removed}本を除去 → 新local {n}本追加 / バンク計 {len(bank)}")
    # 地域の巡回を表示
    from collections import Counter
    areas = Counter(t["segments"][0].split("にお住まい")[0] for t in bank if t.get("local"))
    print("地域の内訳:", dict(areas))


if __name__ == "__main__":
    main()
