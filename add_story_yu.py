def main():
    p = os.path.join(DIR, "content_bank_yu.json")
    bank = json.load(open(p, encoding="utf-8"))
    existing = {t["segments"][0].split("\n")[0].strip() for t in bank}
    n = 0
    for dz, cat, segs, cta in ITEMS:
        if segs[0].split("\n")[0].strip() in existing:
            continue
        bank.append({"type": "story", "cat": cat, "disease": dz,
                     "segments": segs + [cta]})
        n += 1
    json.dump(bank, open(p, "w"), ensure_ascii=False, indent=1)
    print(f"参考型の投稿 {n}本追加 / バンク計 {len(bank)}")


if __name__ == "__main__":
    main()
