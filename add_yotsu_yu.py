def main():
    p = os.path.join(DIR, "content_bank_yu.json")
    bank = json.load(open(p, encoding="utf-8"))
    existing = {t["segments"][0].split("\n")[0].strip() for t in bank}
    n = 0
    for i, (cat, segs) in enumerate(TREES):
        if segs[0].split("\n")[0].strip() in existing:
            continue
        # CTAは腰痛キーワードとソフトを巡回（3本に1本はLINE誘導、他はソフト＝価値バランス）
        cta = CTA_YO[i % len(CTA_YO)] if i % 3 == 0 else CTA_SOFT[i % len(CTA_SOFT)]
        bank.append({"type": "yotsu", "cat": cat, "disease": "腰痛",
                     "segments": segs + [cta]})
        n += 1
    json.dump(bank, open(p, "w"), ensure_ascii=False, indent=1)
    print(f"腰痛の投稿 {n}本追加 / バンク計 {len(bank)}")


if __name__ == "__main__":
    main()
