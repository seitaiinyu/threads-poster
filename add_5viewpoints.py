def add(fn, items, ctas, three_part):
    p = os.path.join(DIR, fn)
    bank = json.load(open(p, encoding="utf-8"))
    existing = {t["segments"][0].split("\n")[0].strip() for t in bank}
    n = ci = 0
    for cat, segs in items:
        if segs[0].split("\n")[0].strip() in existing:
            continue
        tree = {"type": "fiveview", "cat": cat}
        if three_part:
            tree["segments"] = segs
            tree["cta"] = ctas[ci % len(ctas)]
        else:
            tree["segments"] = segs + [ctas[ci % len(ctas)]]
        bank.append(tree); ci += 1; n += 1
    json.dump(bank, open(p, "w"), ensure_ascii=False, indent=1)
    print(f"{fn}: 5視点投稿 {n}本追加 / 計{len(bank)}")


