def main():
    bank = json.load(open(os.path.join(DIR, "content_bank.json"), encoding="utf-8"))
    hp = os.path.join(DIR, "harvested_diet.json")
    harvested = json.load(open(hp)) if os.path.exists(hp) else []
    hset = set(harvested)
    added = 0
    for ci, (src_id, segs) in enumerate(ITEMS):
        if src_id in hset:
            continue
        bank.append({"type": "harvest", "src": src_id, "segments": list(segs) + [CTAS[ci % len(CTAS)]]})
        harvested.append(src_id)
        added += 1
    json.dump(bank, open(os.path.join(DIR, "content_bank.json"), "w"), ensure_ascii=False, indent=1)
    json.dump(harvested, open(hp, "w"), ensure_ascii=False, indent=1)
    print(f"追加: {added}ツリー / バンク合計: {len(bank)} / 改良済み元投稿: {len(harvested)}件")


if __name__ == "__main__":
    main()
